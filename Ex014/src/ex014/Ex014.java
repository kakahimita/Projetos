
package ex014;


public class Ex014 {


    public static void main(String[] args) {
        
        // Estrutura de repetição
        // exemplo com string contagem linear 
        
        int cc = 0;
        String contagem = "";
        while(cc<5) {
            contagem += cc + " ";
            cc++;
        }
        System.out.println(contagem);

    }
    
}
